/**
 * 소통게시판 Excel → 정적 TypeScript 데이터 변환
 *
 * Usage:
 *   node scripts/generate-board-data.mjs [excel-path]
 *
 * Default excel path: Desktop 소통게시판_의사소통기록_20260619 (2).xlsx
 */
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const require = createRequire(import.meta.url);
const XLSX = require("xlsx");

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const defaultExcelPath =
  "c:/Users/midac/Desktop/소통게시판_의사소통기록_20260619 (2).xlsx";
const excelPath = process.argv[2] ?? defaultExcelPath;
const outFile = join(root, "src/data/board/communication-posts.ts");

const VALID_CATEGORIES = ["공지", "버그/오류", "요청사항", "일반"];

function escapeTsString(value) {
  return JSON.stringify(String(value ?? ""));
}

const workbook = XLSX.readFile(excelPath);
const posts = XLSX.utils.sheet_to_json(workbook.Sheets["게시글"]);
const comments = XLSX.utils.sheet_to_json(workbook.Sheets["댓글"]);

const commentsByPost = new Map();
for (const comment of comments) {
  const postNo = comment["게시글번호"];
  if (!commentsByPost.has(postNo)) {
    commentsByPost.set(postNo, []);
  }
  commentsByPost.get(postNo).push(comment);
}

for (const list of commentsByPost.values()) {
  list.sort((a, b) => {
    const seqDiff = (a["댓글순번"] ?? 0) - (b["댓글순번"] ?? 0);
    if (seqDiff !== 0) {
      return seqDiff;
    }
    return String(a["작성일시"] ?? "").localeCompare(
      String(b["작성일시"] ?? ""),
    );
  });
}

const threads = posts.map((post) => {
  const category = post["카테고리"];
  if (!VALID_CATEGORIES.includes(category)) {
    throw new Error(`Unknown category: ${category}`);
  }

  const postNo = post["게시글번호"];
  const postComments = commentsByPost.get(postNo) ?? [];

  const messages = [
    {
      kind: "post",
      author: post["작성자"],
      createdAt: post["작성일시"],
      content: post["내용"],
    },
    ...postComments.map((comment) => ({
      kind: "comment",
      author: comment["작성자"],
      createdAt: comment["작성일시"],
      content: comment["내용"],
    })),
  ];

  return {
    category,
    title: post["제목"],
    pinned: post["고정"] === "Y",
    messages,
    _sortAt: post["작성일시"],
  };
});

threads.sort((a, b) => {
  if (a.pinned !== b.pinned) {
    return a.pinned ? -1 : 1;
  }
  return String(b._sortAt ?? "").localeCompare(String(a._sortAt ?? ""));
});

const threadLines = threads.map((thread) => {
  const messageLines = thread.messages.map((message) => {
    return `      {
        kind: ${escapeTsString(message.kind)},
        author: ${escapeTsString(message.author)},
        createdAt: ${escapeTsString(message.createdAt)},
        content: ${escapeTsString(message.content)},
      }`;
  });

  return `  {
    category: ${escapeTsString(thread.category)},
    title: ${escapeTsString(thread.title)},
    pinned: ${thread.pinned},
    messages: [
${messageLines.join(",\n")}
    ],
  }`;
});

const output = `// Auto-generated by scripts/generate-board-data.mjs — do not edit manually.

export type BoardCategory = "공지" | "버그/오류" | "요청사항" | "일반";

export type BoardMessageKind = "post" | "comment";

export type BoardMessage = {
  kind: BoardMessageKind;
  author: string;
  createdAt: string;
  content: string;
};

export type BoardThread = {
  category: BoardCategory;
  title: string;
  pinned: boolean;
  messages: BoardMessage[];
};

export const boardThreads: BoardThread[] = [
${threadLines.join(",\n")}
];
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, output, "utf8");

console.log(`✅ ${outFile} 생성 (${threads.length} threads)`);
